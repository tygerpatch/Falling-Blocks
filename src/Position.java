package src;

// represent position on board
public class Position 
{
	private int row;
	
	public int getRow()
	{
		return row;
	}
	
	public void setRow(int row)
	{
		this.row = row;
	}
		
	private int column;
	
	public int getColumn()
	{
		return column;
	}
	
	public void setColumn(int column)
	{
		this.column = column;
	}
}
